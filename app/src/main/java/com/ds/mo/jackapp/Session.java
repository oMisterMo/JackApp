package com.ds.mo.jackapp;

public class Session {

    public String day, month, year, location, price;

    public Session() {

    }

    public Session(String day, String month, String year, String location, String price) {
        this.day = day;
        this.month = month;
        this.year = year;
        this.location = location;
        this.price = price;
    }
}
